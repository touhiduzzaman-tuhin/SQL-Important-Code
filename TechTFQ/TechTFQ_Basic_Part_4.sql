-- JOIN ==> INNER JOIN
-- LEFT JOIN ==> LEFT OUTER JOIN
-- LEFT JOIN ==> INNER JOIN + LEFT TABLE VALUE
-- RIGHT JOIN ==> RIGHT OUTER JOIN
-- RIGHT JOIN ==> INNER JOIN + RIGHT TABLE VALUE
-- FULL JOIN ==> FULL OUTER JOIN
-- FULL JOIN ==> INNER JOIN + RIGHT JOIN + LEFT JOIN

