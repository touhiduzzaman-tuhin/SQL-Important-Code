-- INNER JOIN
-- FETCH THE EMPLOYEE NAME AND DEPARTMENT NAME THAT THEY BLONG TO
SELECT * FROM EMPLOYEE;
SELECT * FROM DEPARTMENT;

SELECT EMP.EMP_NAME, DPT.DEPT_NAME
FROM EMPLOYEE AS EMP
JOIN DEPARTMENT AS DPT ON EMP.DEPT_ID = DPT.DEPT_ID;

SELECT EMP.EMP_NAME, DPT.DEPT_NAME
FROM EMPLOYEE AS EMP
INNER JOIN DEPARTMENT AS DPT ON EMP.DEPT_ID = DPT.DEPT_ID
ORDER BY EMP_NAME;


-- LEFT JOIN
-- FETCH ALL THE EMPLOYEE NAME AND THEIR DEPARTMENT NAME THAT THEY ARE 
SELECT E.EMP_NAME, D.DEPT_NAME
FROM EMPLOYEE AS E
LEFT JOIN DEPARTMENT AS D ON E.DEPT_ID = D.DEPT_ID;



-- RIGHT JOIN

SELECT E.EMP_NAME, D.DEPT_NAME
FROM EMPLOYEE AS E
RIGHT JOIN DEPARTMENT AS D ON E.DEPT_ID = D.DEPT_ID;

-- FETCH DETAILS OF ALL EMP, THEIR MANAGER, THEIR DEPARTMENT, AND THE PROJECT THEY WORK ON
SELECT * FROM EMPLOYEE;
SELECT * FROM MANAGER;
SELECT * FROM DEPARTMENT;
SELECT * FROM PROJECTS;

SELECT E.EMP_NAME, D.DEPT_NAME, M.MANAGER_NAME, P.PROJECT_NAME
FROM EMPLOYEE AS E
LEFT JOIN DEPARTMENT AS D ON E.DEPT_ID = D.ID
INNER JOIN MANAGER AS M ON E.MANAGER_ID = M.MANAGER_ID
LEFT JOIN PROJECTS AS P ON P.TEAM_MEMBER_ID = E.EMP_ID;

-- FULL JOIN
-- FATCH ALL EMPLOYEE NAME AND DEPARTMENT NAME
SELECT E.EMP_NAME, D.DEPT_NAME
FROM EMPLOYEE AS E
FULL JOIN DEPARTMENT AS D ON E.DEPT_ID = D.DEPT_ID;

